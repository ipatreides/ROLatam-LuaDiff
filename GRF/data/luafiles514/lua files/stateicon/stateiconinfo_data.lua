local function MergeTables(dest, src)
	for k, v in pairs(src) do
		if type(v) == "table" and type(dest[k]) == "table" then
			MergeTables(dest[k], v)
		else
			dest[k] = v
		end
	end
end
StateIconList = {}
if StateIconList_data then
	for k, v in pairs(StateIconList_data) do
		StateIconList[k] = v
	end
end
if StateIconList_string then
	for k, v in pairs(StateIconList_string) do
		if StateIconList[k] then
			MergeTables(StateIconList[k], v)
		else
			StateIconList[k] = v
		end
	end
end
