local function MergeTables(dest, src)
	for k, v in pairs(src) do
		if type(v) == "table" and type(dest[k]) == "table" then
			MergeTables(dest[k], v)
		else
			dest[k] = v
		end
	end
end
OptionTbl = {}
if OptionTbl_data then
	for k, v in pairs(OptionTbl_data) do
		OptionTbl[k] = v
	end
end
if OptionTbl_string then
	for k, v in pairs(OptionTbl_string) do
		if OptionTbl[k] then
			MergeTables(OptionTbl[k], v)
		end
	end
end
