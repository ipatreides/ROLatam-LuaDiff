local function MergeTables(dest, src)
	for k, v in pairs(src) do
		if type(v) == "table" and type(dest[k]) == "table" then
			MergeTables(dest[k], v)
		else
			dest[k] = v
		end
	end
end
tbl = {}
if tbl_data then
	for k, v in pairs(tbl_data) do
		tbl[k] = v
	end
end
if tbl_string then
	for k, v in pairs(tbl_string) do
		if tbl[k] then
			MergeTables(tbl[k], v)
		end
	end
end
function main()
	for DocumentNum, INFO in pairs(tbl) do
		result, msg = AddTip(DocumentNum, INFO.Title, INFO.Search, INFO.Image)
		if not result then
			return false, msg
		end
		result, msg = AddImgcoord(DocumentNum, INFO.Imgcoord.Position, INFO.Imgcoord.Size)
		if not result then
			return false, msg
		end
		if not INFO.PageEX then
			result, msg = AddPageEx(DocumentNum, -1, 0, 0, 0, 0, 0, 0, 0)
			if not result then
				return false, msg
			end
		else
			for k, v in pairs(INFO.PageEX) do
				result, msg = AddPageEx(DocumentNum, v.EffectNum, v.Twinkle, v.StartX, v.StartY, v.DistX, v.DistY, v.MoveTime)
				if not result then
					return false, msg
				end
			end
		end
		for page, list in ipairs(INFO.Page) do
			local str = ""
			for k, v in ipairs(list) do
				if 0 ~= #str then
					str = str .. "\n"
				end
				str = str .. v
			end
			result, msg = AddPage(DocumentNum, str)
			if not result then
				return false, msg
			end
		end
	end
	return true, "good"
end
